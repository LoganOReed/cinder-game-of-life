Engine::Engine(int w, int h) {
    width = w;
    height = h;

    ages[0] = Color(241.0f / 255.0f, 196.0f / 255.0f, 15.0f / 255.0f);
    ages[1] = Color(230.0f / 255.0f, 126.0f / 255.0f, 34.0 / 255.0f);
    ages[2] = Color(231.0f / 255.0f, 76.0f / 255.0f, 60.0f / 255.0f);
    ages[3] = Color(192.0f / 255.0f, 57.0f / 255.0f, 43.0f / 255.0f);
}

void Engine::InitializeBoard() {
    SetBoardToAllDead();
    MakeRandomBoard();
}

void Engine::Update() {
    for (size_t r = 0; r < NUM_ROWS; r++) {
        for (size_t c = 0; c < NUM_COLS; c++) {
            if (board[r][c] == 0 && GetAliveNumNearby(r, c) == 3) {
                new_board[r][c] = 1;
                colorBoard[r][c] += 1;
            }
            else if (board[r][c] == 1 && (GetAliveNumNearby(r, c) == 3 || GetAliveNumNearby(r, c) == 4)) {
                new_board[r][c] = 1;
                colorBoard[r][c] += 1;
            }
            else if (board[r][c] == 1 && (GetAliveNumNearby(r, c) <= 2 || GetAliveNumNearby(r, c) >= 5)) {
                new_board[r][c] = 0;
                colorBoard[r][c] = -1;
            }
        }
    }
}

void Engine::Draw() {
    cinder::gl::color(Color(245.0 / 255.0, 246.0 / 255.0, 250.0 / 255.0));
    for (size_t r = 0; r < NUM_ROWS; r++) {
        for (size_t c = 0; c < NUM_COLS; c++) {
            if (new_board[r][c] == 1) {
                int color = (colorBoard[r][c] < 4) ? colorBoard[r][c] : 3;
                color = (color > -1) ? color : 0;
                cinder::gl::color(ages[color]);
                gl::drawSolidRect(Rectf(r * length, c * length, r * length + length, c * length + length));
            }
        }
    }
}

int Engine::GetAliveNumNearby(int x, int y) {
    int live_count = 0;
    for (int r = x - 1; r <= x + 1; r++) {
        for (int c = y - 1; c <= y + 1; c++) {
            if (board[r][c] == 1) {
                live_count++;
            }
        }
    }
    return live_count;
}

void Engine::BoardReset() {
    for (int r = 0; r < NUM_ROWS; r++) {
        for (int c = 0; c < NUM_COLS; c++) {
            board[r][c] = new_board[r][c];
            new_board[r][c] = 0;
        }
    }
}

void Engine::SetBoardToAllDead() {
    for (int r = 0; r < NUM_ROWS; r++) {
        for (int c = 0; c < NUM_COLS; c++) {
            board[r][c] = 0;
        }
    }
}

void Engine::MakeRandomBoard() {
    for (int r = 0; r < NUM_ROWS; r++) {
        for (int c = 0; c < NUM_COLS; c++) {
            bool half_prob = (rand() % 100) < 60; // 50% probability
            if (half_prob) {
                board[r][c] = 1;
            }
        }
    }
}

